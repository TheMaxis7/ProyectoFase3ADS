    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Nodo xNodo;
        String nombre, genero, tPresion, tSangre;
        nombre = txtNombre.getText();
        genero = cmbGenero.getItemAt(cmbGenero.getSelectedIndex());
        tSangre = cmbTipoSangre.getItemAt(cmbTipoSangre.getSelectedIndex());
        tPresion = cmbTIpoPresion.getItemAt(cmbTIpoPresion.getSelectedIndex());
        
        
        if(!nombre.equals("") && !genero.equals("<Seleccione>") && !tSangre.equals("<Seleccione>") && !tPresion.equals("<Seleccione>")){
            xNodo = arbolPacientes.ingresarPaciente(nombre, genero, tSangre, tPresion);
            arbolPacientes.TransversaPreO(xNodo);
        
            //Agregamos el Objecto Paciente a la coleccion
            Paciente paciente = new Paciente();
            paciente.setNombre(nombre);
            paciente.setGenero(genero);
            paciente.setT_presion(tPresion);
            paciente.setG_sanguineo(tSangre);
            xPacientes.add(paciente);
            
            JOptionPane.showMessageDialog(null, "Registro completo.");
            //Reiniciamos el formualario
            txtNombre.setText("");
            cmbGenero.setSelectedIndex(0);
            cmbTipoSangre.setSelectedIndex(0);
            cmbTIpoPresion.setSelectedIndex(0);
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

